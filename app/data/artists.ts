// Helper to find artist by slug
export function findArtistBySlug(slug: string): { name: string; pieces: { title: string; url: string }[] } | null {
  for (const [name, pieces] of Object.entries(allPieces)) {
    // Create a simple slug from the name for comparison
    const artistSlug = name.toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/^-|-$/g, '');

    if (artistSlug === slug) {
      return { name, pieces };
    }
  }
  return null;
}
